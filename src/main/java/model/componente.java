package model;

public class componente {
	private int idcadeira;
	private String codcomponente;
	private String nomecomponente;
	private String cargahoraria;
	private String crcomponente;
	private String natureza;
	
	
	public int getIdcadeira() {
		return idcadeira;
	}
	public void setIdcadeira(int idcadeira) {
		this.idcadeira = idcadeira;
	}
	
	
	public String getCodcomponente() {
		return codcomponente;
	}
	public void setCodcomponente(String codcomponente) {
		this.codcomponente = codcomponente;
	}
	
	
	public String getNomecomponente() {
		return nomecomponente;
	}
	public void setNomecomponente(String nomecomponente) {
		this.nomecomponente = nomecomponente;
	}
	
	
	public String getCrcomponente() {
		return crcomponente;
	}
	public void setCrcomponente(String crcomponente) {
		this.crcomponente = crcomponente;
	}
	
	
	public String getCargahoraria() {
		return cargahoraria;
	}
	public void setCargahoraria(String cargahoraria) {
		this.cargahoraria = cargahoraria;
	}
	
	
	public String getNatureza() {
		return natureza;
	}
	public void setNatureza(String natureza) {
		this.natureza = natureza;
	}
	
}
